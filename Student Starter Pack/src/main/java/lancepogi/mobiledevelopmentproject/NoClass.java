public class NoClass {
    private int id;
    private String day, desc;

    public void setId(int id) {
        this.id = id;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getId() {
        return this.id;
    }

    public String getDay() {
        return this.day;
    }

    public String getDesc() {
        return this.desc;
    }
}
